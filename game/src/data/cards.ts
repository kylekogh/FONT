export const ALL_CARDS: CardDefinition[] = [...KINDLED_CARDS, ...NEUTRAL_CARDS];

export function getCardById(id: string): CardDefinition | undefined {
  return ALL_CARDS.find(c => c.id === id);
}

// Returns the starter deck definitions (not instances)
export function getStarterDeckDefs(): CardDefinition[] {
  const strike = getCardById('kindled_strike')!;
  const brace = getCardById('kindled_brace')!;
  const surge = getCardById('kindled_surge')!;
  return [
    strike, strike, strike, strike, strike,
    brace, brace, brace, brace,
    surge,
  ];
}

// Returns draft pool cards (excluding upgraded versions)
export function getDraftPool(): CardDefinition[] {
  return ALL_CARDS.filter(c => !c.upgraded);
}
